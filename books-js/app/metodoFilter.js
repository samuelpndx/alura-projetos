const botoes = document.querySelectorAll('.btn')
botoes.forEach(btn => btn.addEventListener('click', filtarLivros))


function filtarLivros(){
    const elementoBtn = document.getElementById(this.id)
    const categoria = elementoBtn.value
    let livrosFiltrados = categoria == 'disponivel' ? filtrarPorDisponibilidade() : livros.filter(filtrarPorCategoria(categoria))
    exibirlivrosNaTela(livrosFiltrados)
    if (categoria == 'disponivel') {
        const valorTotal = calcularValorTotalDeLivrosDisponiveis(livrosFiltrados)
        exibirValorTotalDosLivrosDisponiveisNaTela(valorTotal)
        
    }
}

function filtrarPorCategoria(categoria) {
    return livro => livro.categoria == categoria
}

function filtrarPorDisponibilidade() {
    return livros.filter(livro => livro.quantidade > 0)
}

function exibirValorTotalDosLivrosDisponiveisNaTela(valorTotal) {
    elementoComValorTotalDeLivroDisponiveis.innerHTML = `
    <div class="livros__disponiveis">
    <p>Todos os livros dispon√≠veis por R$ <span id="valor">${valorTotal}</span></p>
  </div>
    `
}